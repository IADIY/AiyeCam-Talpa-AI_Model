	detect_config.win_f_id_y0 = detect_config.win_f_id_x0;

	/**************************************
	按照支持的最高60HZ配置
	系统不同状态使用不同的大小
	最小模型大小，最大160，必须是8的倍数
	配置最小值必须大于等于全部模型中的最大值
	***************************************/

	detect_config.max_scaler_w[NB_DETECT_SYS_MODE_GLOBAL] = 88;
	detect_config.max_scaler_h[NB_DETECT_SYS_MODE_GLOBAL] = 80;

	detect_config.max_scaler_w[NB_DETECT_SYS_MODE_MOTION] = 88;
	detect_config.max_scaler_h[NB_DETECT_SYS_MODE_MOTION] = 88;

	detect_config.max_scaler_w[NB_DETECT_SYS_MODE_FOUND] = 96;
	detect_config.max_scaler_h[NB_DETECT_SYS_MODE_FOUND] = 80;

	// 暗态(小于等于25fps)边缘强化功能
	detect_config.Dark_edge_enhancement = 0;

	// 是否使用识别
	detect_config.use_found = 1;

	// 识别后接下来几帧跟随
	detect_config.use_found_follow = 1;
	detect_config.use_found_follow_times = 15;		  // 此功能执行的次数
	detect_config.use_found_follow_score = -10000000; // 最低分数

	// 识别范围
	detect_config.detect_max_xpiont = SENSOR_OUTPUT_SIZE; // 结束坐标
	detect_config.detect_max_ypiont = SENSOR_OUTPUT_SIZE;

	// 检测到found，found模式使用最大帧数
	detect_config.mode_found_wait_f = 60;

	// 识别结果，是否使用亮度滤波
	detect_config.b_filter = 1;
	// 平均亮度的极限
	detect_config.b_filter_low_value = 30;
	detect_config.b_filter_hight_value = 220;

	// 特征值滤波   特征值中心区域
	detect_config.eigenvalue_filter = 1;	   // 0 关闭   1 打开
	detect_config.eigenvalue_all_0 = 40;	   // 数值0百分比  超过认为是误识别
	detect_config.eigenvalue_continuous_0 = 4; // 连续0的数量  超过认为是误识别
	detect_config.eigenvalue_bigitem = 20;	   // 大于平均值的百分比 小于是误识别

	detect_config.eigenvalue_max_limit = 32; // 最大值最小阈值 小于是误识别

	detect_config.eigenvalue_norm_limt = 800; // 最小归一化norm值 小于是误识别

	/*
	跟踪功能
	*/
	// 是否使用跟踪
	detect_config.use_track = 0;

	// 跟踪功能使用帧数
	detect_config.use_track_times = 15;
	detect_config.use_track_score = 30;

	// 是否使用motion
	detect_config.use_motion = 1;

	// 检测到motion，motion模式使用最大帧数
	detect_config.mode_motion_wait_f = 100;

	// 识别结果，是否使用motion滤波
	detect_config.motion_filter = 0;

	detect_config.get_buf = 0;

	detect_config.debug_lv = NB_DEBUG_MODE_NO;

	// 多宫格使用配置
	User_Area_Config();

	// 串口和图像输出画框信息
	detect_config.debug_win = 0;

	NB_detect_top_config(detect_config, detect_model, user_area_table, user_winmid);
